# La función 'Range()' en bucles for

#EJEMPLO: Con 1 parámetro
for x in range (10): #Con este bucle la consola devuelve 10 números (del 0 al 9), por defecto incrementa el valor de x. Esto nos ahorra algunas lineas de código
    print(x) #Imprime la variable x

#EJEMPLO: Con 2 parámetros

for x1 in range (5,10): #Con este bucle la consola devuelve 5 numero (del 5 al 9)
    print(x1) #Imprime la variable x1


