#Multiples condiciones if sin ningun else o elif

entrada = int(input('Introduce un numero del 1 al 4:\n')) #La función input permite al usuario interactuar con el programa

if entrada == 1:
    print('Has seleccionado la opcion numero 1.') #Primer condición, de cumplirse se mostrara este texto 
if entrada == 2:
    print('Has seleccionado la opcion numero 2.') #Segunda condición, de cumplirse se mostrara este texto 
if entrada == 3:
    print('Has seleccionado la opcion numero 3.') #Tercer condición, de cumplirse se mostrara este texto 
if entrada == 4:
    print('Has seleccionado la opcion numero 4.') #Cuarta condición, de cumplirse se mostrara este texto 
    
