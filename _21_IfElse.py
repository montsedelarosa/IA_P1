# If Else: 'Else' es un complemento para el condicional if. En caso de que la condición sea verdadera (True) se ejecuta el código dentro del if, en cambio, si es falsa (False) se ejecutará la del else.

edad = 20 #Valor de la variable
edad2 = 16 #Valor de la variable

if edad >= 18: #Condición, solo si es mayor de 18 podrá acceder
    print('ACCESO PERMITIDO, eres mayor de edad.') #Imprimir mensaje en caso de que la condición sea verdadera
else: #Si la primer condición no se cumple se mostrara la del else
    print('ACCESO DENEGADO, eres menor de edad.') #Imprime mensaje en caso de que la condición inicial sea falsa

if edad2 >= 18: #Condición, solo si es mayor de 18 podrá acceder
    print('ACCESO PERMITIDO, eres mayor de edad.') #Imprimir mensaje en caso de que la condición sea verdadera
else: #Si la primer condición no se cumple se mostrara la del else
    print('ACCESO DENEGADO, eres menor de edad.') #Imprime mensaje en caso de que la condición inicial sea falsa